function memberList() {
    let memberListHTMl = ""

    DataMemberList.forEach((name) => {
        memberListHTMl += `<button class=" rounded-[6px] px-[12px] py-[4px] bg-pink-200 hover:bg-pink-600" style="border: 1px solid #DB2777;" onclick="filterData('${name}')">
        <p class="text-pink-700 hover:text-slate-200">${name}</p>
    </button>`
    })
    document.querySelector(".member-list").innerHTML = memberListHTMl

}

memberList()

function allTestimonial() {
    let testimonialHTML = ""

    dataMember.forEach((card) => {
        testimonialHTML += `
        <img class="h-auto w-[256px] object-cover" src="${card.images}" alt="" data-filter="${card.Member}">`
    })

    document.querySelector(".galery-masonary").innerHTML = testimonialHTML
}

allTestimonial()

function filterData(Member) {
    if (Member === "All") {
        return allTestimonial()
    }
    let filterDataHTML = "";

    const filterTestimonial = dataMember.filter((card) => {
        return card.Member === Member;
    });

    filterTestimonial.forEach((card) => {
        filterDataHTML += `<img  class="h-auto w-[256px] object-cover" src="${card.images}" alt="" data-filter="${card.Member}">`;
    });

    document.querySelector(".galery-masonary").innerHTML = filterDataHTML;
}